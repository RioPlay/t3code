#!/usr/bin/env node
import * as NodeFS from "node:fs";
import * as NodeModule from "node:module";
import * as NodePath from "node:path";
import * as NodeURL from "node:url";

const scriptDirectory = NodePath.dirname(NodeURL.fileURLToPath(import.meta.url));
const mobileRoot = NodePath.resolve(scriptDirectory, "..");
const workspaceRoot = NodePath.resolve(mobileRoot, "../..");
const require = NodeModule.createRequire(NodePath.join(workspaceRoot, "apps/web/package.json"));

const xtermRoot = NodePath.dirname(require.resolve("@xterm/xterm/package.json"));
const fitRoot = NodePath.dirname(require.resolve("@xterm/addon-fit/package.json"));

const xtermJs = NodeFS.readFileSync(NodePath.join(xtermRoot, "lib/xterm.js"), "utf8");
const xtermCss = NodeFS.readFileSync(NodePath.join(xtermRoot, "css/xterm.css"), "utf8");
const fitJs = NodeFS.readFileSync(NodePath.join(fitRoot, "lib/addon-fit.js"), "utf8");

const bridgeRuntime = String.raw`
(function () {
  const post = (payload) => {
    const bridge = window.ReactNativeWebView;
    if (!bridge || typeof bridge.postMessage !== "function") {
      return;
    }
    bridge.postMessage(JSON.stringify(payload));
  };

  const stripOsc8Hyperlinks = (chunk) =>
    chunk.replace(/\x1b\]8;[^\x07\x1b]*(?:\x07|\x1b\\)/g, "");

  let term = null;
  let fitAddon = null;
  let configured = false;
  let pendingBuffer = "";
  let resizeObserver = null;

  const ensureTerminal = () => {
    if (term) {
      return term;
    }
    const TerminalCtor = window.Terminal;
    const FitAddonCtor = window.FitAddon && window.FitAddon.FitAddon;
    if (!TerminalCtor || !FitAddonCtor) {
      return null;
    }
    term = new TerminalCtor({
      cursorBlink: true,
      convertEol: true,
      disableStdin: false,
      allowProposedApi: false,
      scrollback: 5000,
      fontFamily: "monospace",
    });
    fitAddon = new FitAddonCtor();
    term.loadAddon(fitAddon);
    term.open(document.getElementById("terminal"));
    term.onData((data) => post({ type: "input", data }));
    const fit = () => {
      try {
        fitAddon.fit();
      } catch (_error) {
        return;
      }
      post({ type: "resize", cols: term.cols, rows: term.rows });
    };
    window.addEventListener("resize", fit);
    if (typeof ResizeObserver === "function") {
      resizeObserver = new ResizeObserver(fit);
      resizeObserver.observe(document.getElementById("terminal"));
    }
    fit();
    post({ type: "ready" });
    return term;
  };

  window.__T3_TERMINAL__ = {
    configure(input) {
      const active = ensureTerminal();
      if (!active) {
        return;
      }
      configured = true;
      active.options.fontSize = input.fontSize;
      active.options.theme = input.theme;
      active.options.disableStdin = !input.isRunning;
      if (pendingBuffer.length > 0) {
        active.write(stripOsc8Hyperlinks(pendingBuffer));
        pendingBuffer = "";
      }
      try {
        fitAddon.fit();
        post({ type: "resize", cols: active.cols, rows: active.rows });
      } catch (_error) {
        // Ignore fit failures during early layout.
      }
    },
    write(data) {
      const sanitized = stripOsc8Hyperlinks(String(data ?? ""));
      const active = ensureTerminal();
      if (!active || !configured) {
        pendingBuffer += sanitized;
        return;
      }
      active.write(sanitized);
    },
    clear() {
      pendingBuffer = "";
      if (term) {
        term.clear();
      }
    },
    focus() {
      if (term) {
        term.focus();
      }
    },
  };
})();
`;

const html = `<!DOCTYPE html>
<html lang="en">
  <head>
    <meta charset="utf-8" />
    <meta
      name="viewport"
      content="width=device-width, initial-scale=1, maximum-scale=1, user-scalable=no"
    />
    <style>${xtermCss}
html, body { margin: 0; padding: 0; height: 100%; overflow: hidden; background: #0a0a0a; }
#terminal { height: 100%; width: 100%; }
    </style>
  </head>
  <body>
    <div id="terminal"></div>
    <script>${xtermJs}</script>
    <script>${fitJs}</script>
    <script>${bridgeRuntime}</script>
  </body>
</html>`;

const outputPath = NodePath.join(
  mobileRoot,
  "src/features/terminal/webView/terminalShellDocument.ts",
);
NodeFS.mkdirSync(NodePath.dirname(outputPath), { recursive: true });
NodeFS.writeFileSync(
  outputPath,
  `// Generated by apps/mobile/scripts/sync-terminal-webview-assets.mjs — do not edit by hand.\nexport const TERMINAL_WEBVIEW_SHELL_HTML = ${JSON.stringify(html)} as const;\n`,
  "utf8",
);

console.log(`Wrote ${outputPath} (${html.length} bytes)`);
